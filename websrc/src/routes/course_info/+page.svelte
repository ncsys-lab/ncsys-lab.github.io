<h1>Course Info</h1>
<h2>Lectures</h2>
We will publish lecture notes as well as slides for each lecture for anyone who misses or cannot attend
in-person class. While our room does have a recording system, the audio and video quality doesn't seem
to be great. We'll post what we have on Canvas, but we'd still strongly recommend attending in person
if you can---we can't make any guarantees about the quality (or even existence) of the recording for
any given lecture.

<h2>Assignments</h2>
There will be 8 weekly assignments for the course as well as a final project. Assignments will usually
be due on Mondays at 11:59 PM, unless indicated otherwise on the assignment handout or the course schedule.
All assignments will be submitted on Gradescope. Each student will be allowed a total of 3 late days
for assignments, which may be spent in units of one day (24 hours) on any assignment throughout the quarter.
There will be no exams in this course.

<h2>Office Hours</h2>
Our office hours will be held by instructors at a time to be determined. They will be a mix of in-person
and remote. You can also get help on
<a target="_blank" rel="noreferrer nofollow noopener" href="https://edstem.org/us/courses/33103"
	>Ed</a
>.

<h2>Grading Policy</h2>
CS45 is graded on a Satisfactory / No Credit basis. We expect that everyone should be able to earn a
grade of satisfactory. Your course grade will be based on your assignment scores, final project score,
and weekly surveys. You get an S if you earn 25 points in the class:
<ul>
	<li>Each weekly assignment (1 thru 8) is worth 3 points (Assignment 0 is worth 1 point).</li>
	<li>Each weekly survey is worth 0.5 points.</li>
	<li>The final project is worth 5 points</li>
</ul>

<h2>Honor Code</h2>
<p>
	As in all Stanford classes, you are expected to follow the Stanford Honor Code. Work submitted for
	grading should not be derived from or influenced by the work of others. Do your own thinking, your
	own design, your own coding, and your own debugging. Any assistance you receive must remain within
	acceptable limits. Truthful citations must be made where required. All submissions are subject to
	plagiarism detection tools. Suspected violations are referred to the Community Standards office.
</p>

<h2>Students with Documented Disabilities</h2>
<p>
	Students who may need an academic accommodation based on the impact of a disability must initiate
	the request with the Student Disability Resource Center (SDRC) located within the Office of
	Accessible Education (OAE). SDRC staff will evaluate the request with required documentation,
	recommend reasonable accommodations, and prepare an Accommodation Letter for faculty dated in the
	current quarter in which the request is being made. Students should contact the SDRC as soon as
	possible since timely notice is needed to coordinate accommodations. The OAE is located at 563
	Salvatierra Walk.
</p>
